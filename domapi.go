func buildNodeType(runtime *goja.Runtime) {
	global := runtime.GlobalObject()
	nodeType := runtime.NewObject()

	nodeType.Set("ErrorNode", html.ErrorNode)
	nodeType.Set("TextNode", html.TextNode)
	nodeType.Set("DocumentNode", html.DoctypeNode)
	nodeType.Set("ElementNode", html.ElementNode)
	nodeType.Set("CommentNode", html.CommentNode)
	nodeType.Set("DoctypeNode", html.DoctypeNode)

	global.Set("NodeType", nodeType)
}

